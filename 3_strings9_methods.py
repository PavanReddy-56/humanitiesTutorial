# There are many string methods. Here are a few:
numString = "7"

# Checking up on the value
# Is it alphanumeric?
print(numString.isalnum())
# Is it a decimal number?
print(numString.isdecimal())
# Is it numeric?
print(numString.isnumeric())
# Is it alphabetical?
print(numString.isalpha())

# Capitalize a sentence
myString = "this is lower case."
print(myString.capitalize())

# Make lower case.
shout = "THIS SURE SEEMS LIKE SHOUTING"
print(shout.lower())

# Make upper case.
notShout = "this didn't start as a shout"
print(shout.upper())