# get a substring
myString = "Hello, my name is Paul!"

# Indexes for strings (and other python objects) start at 0!
# Print the first character
print(myString[0])

# Print the last character using -1
print(myString[-1])

# Print the first 10 characters:
# Not that this will print characters at indexs 0-9 (and NOT 10)!
print(myString[:10])

# Print the characters at indexes 4 to 12:
print(myString[4:13])

# Print the characters from indexes 8 to the end:
print(myString[8:])